/* ===================== OBJETOS GLOBALES ===================== */
Adafruit_SSD1306 display(SCREEN_WIDTH, SCREEN_HEIGHT, &Wire, -1);

/* ===================== VARIABLES DE ESTADO ===================== */
int currentPokemonID = 0;
bool showStatsMode = false;

// Variables para debounce de botones
int lastVoz = HIGH;
int lastGrito = HIGH;
int lastStats = HIGH;
int lastMusic = HIGH;
unsigned long lastDebounceTime = 0;
const unsigned long debounceDelay = 200;

/* ===================== FUNCIONES ===================== */

void drawInterface() {
  display.clearDisplay();
  
  if (!showStatsMode) {
    // === VISTA 1: SPRITE ===
    display.setTextSize(1);
    display.setCursor(0, 0);
    display.print("POKEDEX");
    
    display.drawBitmap(110, 0, pokeball_icon, 16, 16, WHITE);

    int spriteX = (SCREEN_WIDTH - 32) / 2;
    int spriteY = 16;
    display.drawBitmap(spriteX, spriteY, pokedex[currentPokemonID].sprite, 32, 32, WHITE);

    display.setCursor(0, 52);
    display.print("#");
    if (currentPokemonID + 1 < 10) display.print("00");
    else if (currentPokemonID + 1 < 100) display.print("0");
    display.print(currentPokemonID + 1); 
    
    display.print(" ");
    display.print(pokedex[currentPokemonID].nombre);
    
  } else {
    // === VISTA 2: ESTADÍSTICAS ===
    display.setTextSize(1);
    display.setCursor(0, 0);
    display.print("DATOS: "); 
    display.print(pokedex[currentPokemonID].nombre);
    display.drawLine(0, 10, 128, 10, WHITE);

    display.setCursor(0, 15);
    display.print("TIPO:"); 
    display.setCursor(40, 15);
    display.print(pokedex[currentPokemonID].tipo);

    display.setCursor(0, 25);
    display.print("ALT.:");
    display.setCursor(40, 25);
    display.print(pokedex[currentPokemonID].altura);

    display.setCursor(0, 35);
    display.print("PESO:");
    display.setCursor(40, 35);
    display.print(pokedex[currentPokemonID].peso);

    display.setCursor(0, 45);
    display.print("ATQ:");
    display.setCursor(40, 45);
    display.print(pokedex[currentPokemonID].ataque);

    display.setCursor(0, 55);
    display.print("DEF:");
    display.setCursor(40, 55);
    display.print(pokedex[currentPokemonID].defensa);
  }
  
  display.display();
}

/* ===================== SETUP ===================== */
void setup() {
  Serial.begin(115200);
  delay(100); // Dar tiempo al Serial en Wokwi
  
  Serial.println("\n\n*** INICIANDO POKEDEX ***");
  Serial.flush();
  
  pinMode(POT_PIN, INPUT);
  pinMode(BTN_VOZ_PIN, INPUT_PULLUP);
  pinMode(BTN_GRITO_PIN, INPUT_PULLUP);
  pinMode(BTN_STATS_PIN, INPUT_PULLUP);
  pinMode(BTN_MUSIC_PIN, INPUT_PULLUP);
  
  Serial.println("Pines configurados");
  Serial.flush();

  if(!display.begin(SSD1306_SWITCHCAPVCC, 0x3C)) {
    Serial.println("Error OLED");
    while(1);
  }
  
  Serial.println("OLED inicializada");
  Serial.flush();
  
  display.clearDisplay();
  display.setTextColor(WHITE);
  display.setTextSize(1);
  display.setCursor(20, 28);
  display.print("POKEDEX V1.0");
  display.display();
  delay(1500);
  
  Serial.println("=================================");
  Serial.println("   POKEDEX INICIADA");
  Serial.println("=================================");
  Serial.println("Gira el potenciometro para cambiar");
  Serial.println("Presiona botones para acciones:");
  Serial.println("  - ROJO: Descripcion de voz");
  Serial.println("  - AZUL: Grito Pokemon");
  Serial.println("  - VERDE: Cambiar vista");
  Serial.println("  - AMARILLO: Musica");
  Serial.println("=================================");
  Serial.flush();
  
  drawInterface();
  Serial.println("Setup completo - Listo para usar\n");
  Serial.flush();
}

/* ===================== LOOP ===================== */
void loop() {
  static int lastPokemonID = -1;
  static bool lastStatsMode = false;
  static bool firstLoop = true;
  
  // Debug: verificar que el loop está corriendo
  if (firstLoop) {
    Serial.println("\n>>> LOOP INICIADO <<<");
    Serial.flush();
    firstLoop = false;
  }
  
  // ============ LEER POTENCIÓMETRO ============
  int potValue = analogRead(POT_PIN);
  int newID = map(potValue, 0, 4095, 0, TOTAL_POKEMON - 1);
  newID = constrain(newID, 0, TOTAL_POKEMON - 1);
  
  if (newID != currentPokemonID) {
    currentPokemonID = newID;
    showStatsMode = false; // Reset a vista sprite
    Serial.print("Seleccionado: ");
    Serial.print(pokedex[currentPokemonID].nombre);
    Serial.print(" (#");
    Serial.print(currentPokemonID + 1);
    Serial.println(")");
    Serial.flush();
  }
  
  // ============ LEER BOTONES ============
  int btnVoz = digitalRead(BTN_VOZ_PIN);
  int btnGrito = digitalRead(BTN_GRITO_PIN);
  int btnStats = digitalRead(BTN_STATS_PIN);
  int btnMusic = digitalRead(BTN_MUSIC_PIN);
  
  unsigned long currentTime = millis();
  
  // Botón VOZ
  if (lastVoz == HIGH && btnVoz == LOW && (currentTime - lastDebounceTime) > debounceDelay) {
    int track = (currentPokemonID * 3) + 1;
    Serial.println("\n=== BOTON VOZ ===");
    Serial.print("DESCRIPCION: ");
    Serial.println(pokedex[currentPokemonID].nombre);
    Serial.print("[Track ");
    Serial.print(track);
    Serial.println(".mp3]");
    Serial.flush();
    lastDebounceTime = currentTime;
  }
  
  // Botón GRITO
  if (lastGrito == HIGH && btnGrito == LOW && (currentTime - lastDebounceTime) > debounceDelay) {
    int track = (currentPokemonID * 3) + 2;
    Serial.println("\n=== BOTON GRITO ===");
    Serial.print("GRITO: ");
    Serial.print(pokedex[currentPokemonID].nombre);
    Serial.println("!");
    Serial.print("[Track ");
    Serial.print(track);
    Serial.println(".mp3]");
    Serial.flush();
    lastDebounceTime = currentTime;
  }
  
  // Botón STATS (cambiar vista)
  if (lastStats == HIGH && btnStats == LOW && (currentTime - lastDebounceTime) > debounceDelay) {
    showStatsMode = !showStatsMode;
    Serial.print("\n=== BOTON STATS PRESIONADO === Vista: ");
    Serial.println(showStatsMode ? "ESTADISTICAS" : "SPRITE");
    Serial.flush();
    
    // FORZAR REDIBUJADO INMEDIATO
    drawInterface();
    lastStatsMode = showStatsMode; // Actualizar para evitar doble redibujado
    
    lastDebounceTime = currentTime;
  }
  
  // Botón MÚSICA
  if (lastMusic == HIGH && btnMusic == LOW && (currentTime - lastDebounceTime) > debounceDelay) {
    int track = (currentPokemonID * 3) + 3;
    Serial.println("\n=== BOTON MUSICA ===");
    Serial.print("MUSICA: Tema de ");
    Serial.println(pokedex[currentPokemonID].nombre);
    Serial.print("[Track ");
    Serial.print(track);
    Serial.println(".mp3]");
    Serial.flush();
    lastDebounceTime = currentTime;
  }
  
  // Guardar estados anteriores
  lastVoz = btnVoz;
  lastGrito = btnGrito;
  lastStats = btnStats;
  lastMusic = btnMusic;
  
  // ============ ACTUALIZAR PANTALLA ============
  if (currentPokemonID != lastPokemonID || showStatsMode != lastStatsMode) {
    drawInterface();
    lastPokemonID = currentPokemonID;
    lastStatsMode = showStatsMode;
  }
  
  delay(50); // Small delay para estabilidad
}
